// Wide Casting (Automatic)
public class Main {
    public static void main(String[] args) {
    int myInt = 9;
    double myDouble = myInt; // Automatic casting: int to double
    System.out.println("myInt: " + myInt); // Output: 9
    System.out.println("myDouble: " + myDouble); // Output: 9.0
}
}