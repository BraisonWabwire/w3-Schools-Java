// The else if condition in Java
public class Main {
    public static void main(String[] args){
        int weather = 4;
        if (weather == 1){
            System.out.println("Bring an umbrella.");
    
        }
        else if (weather == 2){
            System.out.println("Wear sun glasses.");
        }
        else{
            System.out.println("Go outside and enjoy the weather.");
        }
    }
}